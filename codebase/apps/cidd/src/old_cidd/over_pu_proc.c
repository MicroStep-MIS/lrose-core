/*************************************************************************
 * OVER_PU_PROC.C - Notify and event callback functions for the overlay selector
 */

#include "cidd.h"


/*************************************************************************
 * Notify callback function for `over_pu_st'.
 * Turn on and off individual overlays 
 */
void
over_pu_proc(item, value, event)
	Panel_item	item;
	unsigned int	value;
	Event		*event;
{
	short	i;
	
	for (i = 0; i < gd.num_map_overlays && i < 32; i++) {
		if (value & 01) {
			gd.over[i]->active = 1;
		} else {
			gd.over[i]->active = 0;
		}

		value >>= 1;
	}
	set_redraw_flags(1,0);
}

