/**********************************************************************
 * Function:       exit_str
 *
 * Description:
 *    Writes out error message and exists the program
 *
 * Input:  
 *    a string which describes why an error is fatal
 * Output: 
 *    exit
 **********************************************************************/

#include "ac_tape_recover.h"


void exit_str(char *errstr)

{

   fflush(stdout);
   fflush(stderr);

   fprintf(stderr,"\nThe following fatal error has occurred:\n\n");
   fprintf(stderr,"\t%s\n\n",errstr);
   fprintf(stderr,"\t\t\t\t Exit.\n");

   exit(ERROR);

}
