#include "clutter_remove.h"

void process_file(char *input_file_path)

{

  PMU_auto_register("In process_file");

  if (Glob->params.debug >= DEBUG_NORM) {
    fprintf(stderr, "Processing input file %s\n", input_file_path);
  }

  if (Glob->params.use_clutter_table) {

    remove_using_table(input_file_path);

  } else {

    remove_using_volume(input_file_path);
    
  }

  return;

}

