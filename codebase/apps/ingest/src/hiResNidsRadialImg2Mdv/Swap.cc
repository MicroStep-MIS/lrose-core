#include "Swap.hh"

void Swap::swap2(void *c){

  unsigned char *b = (unsigned char *) c;

  unsigned char t = *b;

  *b = *(b+1);

  *(b+1) = t;

  return;
}

void Swap::swap4(void *c){

  unsigned char *b = (unsigned char *) c;

  unsigned char t = *b;
  *b = *(b+3);
  *(b+3) = t;

  t = *(b+1);
  *(b+1) = *(b+2);
  *(b+2) = t;

  return;
}

